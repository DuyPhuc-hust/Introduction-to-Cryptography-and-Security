from typing import Dict, Any

def mod_inverse(a: int, m: int) -> int:
    g, x, _ = egcd(a, m)
    if g != 1:
        raise ValueError("Modular inverse does not exist")
    return x % m

def egcd(a: int, b: int):
    if a == 0:
        return b, 0, 1
    g, y, x = egcd(b % a, a)
    return g, x - (b // a) * y, y

def recover_private_key(data: Dict[str, Any]) -> int:
    p = int(data["p"])
    q = int(data["q"])
    g = int(data["g"])
    y = int(data["y"])

    sig1 = data["sig1"]
    sig2 = data["sig2"]

    x1 = int(sig1["x"])
    r1 = int(sig1["r"])
    s1 = int(sig1["s"])

    x2 = int(sig2["x"])
    r2 = int(sig2["r"])
    s2 = int(sig2["s"])

    # We have two equations:
    # s1 * k ≡ x1 - d * r1  (mod q)    =>   s1*k + r1*d ≡ x1 (mod q)
    # s2 * (k+1) ≡ x2 - d * r2 (mod q) =>   s2*k + s2 + r2*d ≡ x2 (mod q)

    # (1)  s1*k + r1*d ≡ x1 (mod q)
    # (2)  s2*k + r2*d ≡ x2 - s2 (mod q)

    A1 = s1 % q
    B1 = r1 % q
    C1 = x1 % q

    A2 = s2 % q
    B2 = r2 % q
    C2 = (x2 - s2) % q

    # Eliminate k:
    # Multiply (1) by A2 and (2) by A1:
    # A2*s1*k + A2*r1*d ≡ A2*x1
    # A1*s2*k + A1*r2*d ≡ A1*(x2 - s2)

    # Subtract them: (A2*r1 - A1*r2)*d ≡ A2*x1 - A1*(x2 - s2) (mod q)

    left = (A2 * B1 - A1 * B2) % q
    right = (A2 * C1 - A1 * C2) % q

    # Now solve: left * d ≡ right (mod q)
    if left == 0:
        raise ValueError("Bad data")
    d = (right * mod_inverse(left, q)) % q

    return d

